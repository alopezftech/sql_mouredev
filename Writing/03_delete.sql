DELETE FROM users WHERE user_id = 1; // Elimina el usuario con ID 1 de la tabla users.

// DEFINICION DE DELETE:

    => La cláusula DELETE se utiliza en SQL para eliminar registros existentes de una tabla.

    // Importancia del WHERE
    
    => Es crucial utilizar la cláusula WHERE en una sentencia DELETE para especificar qué registros se deben eliminar. Sin un WHERE, se eliminarían todos los registros de la tabla.