SELECT * FROM users WHERE mail IS NULL; // Consulta todos los usuarios cuyo correo electrónico es nulo

SELECT * FROM users WHERE mail IS NOT NULL; // Consulta todos los usuarios cuyo correo electrónico no es nulo

// DEFINICION DE NULL:
 
    => La cláusula NULL se utiliza en una consulta SQL para verificar si un valor es nulo (es decir, no tiene valor asignado).